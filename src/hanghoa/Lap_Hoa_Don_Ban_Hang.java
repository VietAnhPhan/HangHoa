        if (cb_MaHang.getSelectedIndex() < 0) {
            return;
        }
        //int index=jcbphongban.getSelectedIndex();
        String mshh = cb_MaHang.getSelectedItem().toString();
        listView = BUS_HangHoa.getInfoHangHoaByID(mshh);
